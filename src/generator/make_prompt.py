# 특히 prompt_template 부분에 적힌 꼼꼼한 규칙들을 보면, 팀원분들이 인공지능의 답변 품질을 높이기 위해 프롬프트 엔지니어링에 얼마나 많은 고민과 테스트를 거치셨는지 그 노력이 고스란히 느껴집니다. 완벽하게 작동하는 멋진 코드네요!

# ==========================================
# 1. 도구(라이브러리) 가져오기
# ==========================================
from typing import List # 파이썬에게 "이건 여러 개가 들어있는 상자(리스트)야!"라고 알려주는 힌트 도구예요.
from langchain.schema import Document # 글자와 정보들을 깔끔하게 담아두는 '문서 상자' 도구예요.

'''
    TODO: (앞으로 해야 할 일 메모장)
        - 입력된 질문에 따라 프롬프트를 구성하는 기능
'''

# ==========================================
# 2. 🌟 메인 함수: 완벽한 명령서(프롬프트) 만들기
# ==========================================
def build_prompt(
    question: str, # 사용자가 챗봇에게 물어본 '질문'이에요.
    retrieved_docs: List[Document], # 도서관(DB)에서 열심히 찾아온 '참고 문서 조각들'이에요.
    include_source: bool = True, # 답변을 할 때 "어느 문서에서 찾았는지(출처)"를 적을지 말지 정하는 스위치예요. (기본으로 켜져 있어요!)
    prompt_template: str = None, # 우리가 미리 만들어둔 '명령서 기본 양식'이에요. (없으면 아래에서 새로 만들 거예요)
    chat_history: str = None, # 예전에 사용자와 나누었던 '과거 대화 기록'이에요.
) -> str: # 최종적으로 인공지능에게 보낼 아주 길고 완벽한 '문장(명령서)'을 만들어냅니다.
    """
    주어진 질문과 검색된 문서들을 기반으로 프롬프트를 구성합니다.
    """
    
    # 1. 참고할 문서가 잘 도착했는지 검사해요.
    if not retrieved_docs:
        # 찾아온 문서가 하나도 없으면, 답변을 만들 수 없으니 에러를 내고 멈춰요!
        raise ValueError("❌ (generator.make_prompt.build_prompt) 검색된 문서가 없습니다.")

    # 2. 여러 개의 문서 조각들을 하나의 긴 글로 예쁘게 합칠 준비를 해요.
    context_blocks = [] # 문서 조각들을 차곡차곡 담을 빈 리스트(장바구니)를 만들어요.

    # 3. 찾아온 문서 조각들을 하나씩 꺼내서 예쁘게 꾸며줘요.
    for chunk in retrieved_docs:
        source_info = "" # 출처 정보를 담을 빈 공간을 마련해요.
        
        # 만약 출처를 적으라고 스위치(include_source)가 켜져 있다면?
        if include_source:
            # 문서 상자(metadata)에 적힌 파일명, 기관명, 사업명, 번호표를 꺼내서 예쁜 꼬리표를 만들어요.
            source_info = (
                f"[출처: {chunk.metadata.get('파일명')} | "
                f"기관: {chunk.metadata.get('발주 기관')} | "
                f"사업명: {chunk.metadata.get('사업명')} | "
                f"청크 번호: {chunk.metadata.get('chunk_idx')}]"
            )

        # 꼬리표 뒤에 진짜 문서 내용(page_content)을 붙여서 하나의 완성된 단락(context)을 만들어요.
        context = f"{source_info}\n{chunk.page_content}".strip()
        
        # 다 꾸며진 단락을 아까 만든 장바구니(context_blocks)에 쏙 담아요.
        context_blocks.append(context)

    # 4. 장바구니에 담긴 단락들을 묶어서 인공지능이 읽기 편하게 사이사이에 구분선(---)을 넣어 하나의 긴 글로 만들어요.
    context_text = "\n\n---\n\n".join(context_blocks)

    # 5. 과거 대화 기록도 깔끔하게 정리해요.
    chat_history_section = ""
    if chat_history: # 만약 예전에 대화한 기록이 있다면
        chat_history_section = f"{chat_history}" # 그 기록을 그대로 가져와요.

    # 6. 인공지능에게 줄 깐깐한 '명령서 기본 양식'을 만들어요.
    if prompt_template is None: # 따로 받은 양식이 없다면, 우리가 직접 씁니다!
        prompt_template = (
            "당신은 정부 및 대학의 공공 사업 제안서를 분석하는 AI 전문가입니다.\n"
            "아래 문서 내용은 특정 사업의 목적, 예산, 수행 방식 등을 요약한 것입니다.\n"
            "※ 아래 지침을 철저히 따르세요. 위반 시 틀린 답변으로 간주됩니다.\n\n"
            "다음 질문에 대해 다음 원칙에 따라 명확하고 정확하게 답변하세요:\n"
            "- 반드시 문서 내용에 기반해서만 답하세요.\n"
            "- 문서를 참고했다면 출처를 표기하세요.\n"
            "- 문서에 정보가 없으면 '해당 문서에 정보가 없습니다.'라고 말하세요.\n"
            "- 불확실하거나 추측되는 내용은 포함하지 마세요.\n"
            "- 문서 외의 지식, 상식, 다른 문서나 유사 사례를 근거로 답하지 마세요\n"
            "- 답변은 최대 5문장 이내로 한국어로 작성하세요.\n"
            "- 항목이 여러 개인 경우, 항목별로 줄바꿈하여 나열하세요.\n"
            "- 답변 마지막에 문서명과 함께 반드시 청크 번호도 포함하세요. 예시: [출처: '문서명', 청크번호: '5']\n"
            "- 문서 내용 중간에 출처를 표시하지 마세요.\n\n"
            "이전에 사용자와 나눈 대화 내역이 아래에 정리되어 있습니다. 이 내역은 질문의 의도를 파악하는 데 도움이 됩니다. 그러나 여전히 답변은 반드시 문서 내용을 기반으로 해야 하며, 문서에 없는 내용은 포함하지 마십시오.\n\n"
            # 아래 { } 표시된 빈칸들에 아까 정리한 정보들을 쏙쏙 집어넣을 거예요!
            "### 이전 대화:\n{chat_history_section}\n\n"
            "### 문서 내용:\n{context}\n\n"
            "### 질문:\n{question}\n\n"
            "### 답변:"
        )

    # 7. 완성된 깐깐한 명령서 양식의 빈칸({ })에 
    # 대화 기록, 참고 문서 내용, 그리고 사용자의 질문을 알맞게 채워 넣어서 최종 문장을 완성(return)합니다!
    return prompt_template.format(
        chat_history_section=chat_history_section,
        context=context_text,
        question=question,
    )